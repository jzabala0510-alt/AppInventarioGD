		GROUP BY
				ISNULL(CC.CODARTICULO, CV.CODARTICULO) 
				, ISNULL(CC.COLOR, CV.COLOR)
				, ISNULL(CC.TALLA, CV.TALLA)
				, ISNULL(CC.UNIDADES,0)
				, ISNULL(CC.HORA, GETDATE())

