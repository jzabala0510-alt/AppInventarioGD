		FROM
				rip.CONTEOLIN CL WITH(NOLOCK)
		WHERE
				CL.FECHA=@FECHA
				AND CL.CODALMACEN=@ALMACEN
				AND (CL.CODUSUARIO=@CODUSUARIO OR @CODUSUARIO=-1)
