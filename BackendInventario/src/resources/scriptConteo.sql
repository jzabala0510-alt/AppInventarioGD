SELECT
		CASE WHEN AL.TALLA IN ('@') THEN '' ELSE  AL.CODBARRAS END CODBARRAS
		, MAX(AL.CODBARRAS2) CODBARRAS2
		, ART.CODARTICULO
		, ART.REFPROVEEDOR
		, ART.DESCRIPCION
		, ISNULL(ART.DESCRIPADIC,'') ADICIONAL
		, ISNULL(DPTO.DESCRIPCION,'SIN DPTO') DEPARTAMENTO
		, ISNULL(S.DESCRIPCION,'SIN SECCION') SECCION
		, ISNULL(F.DESCRIPCION,'') FAMILIA
		, ISNULL(SF.DESCRIPCION,'') SUBFAMILIA
		, ISNULL(M.DESCRIPCION,'') MARCA
		, CASE WHEN AL.TALLA IN ('@') THEN '.' ELSE  AL.TALLA END TALLA
		, CASE WHEN AL.TALLA IN ('@') THEN '.' ELSE  AL.COLOR END  COLOR
		, CZ.ZONA_1
		, CZ.ZONA_2
		, CZ.ZONA_3
		, CZ.ZONA_4
		, CZ.ZONA_5
		, CZ.ZONA_6
		, CZ.ZONA_7
		, ROUND(SUM(ISNULL(CL.UNIDADES,0)),3)  CONTADO
		, ROUND(SUM(ISNULL(ST.STOCK,0)),3) STOCK
		, CASE WHEN ROUND(SUM(ISNULL(CL.UNIDADES,0)),3)=0 AND ROUND(SUM(ISNULL(CL.UNIDADES,0)),3)=0 
			THEN SUM(ISNULL(VEN.VENDIDO,0)) ELSE  ROUND(SUM(ISNULL(ISNULL(CL.VENDIDAS,0),0)),3) END VENDIDAS
		, CASE WHEN ROUND(SUM(ISNULL(CL.UNIDADES,0)),3)=0 AND ROUND(SUM(ISNULL(CL.UNIDADES,0)),3)=0 
			THEN SUM(ISNULL(ST.STOCK,0))-ROUND(SUM(ISNULL(ISNULL(VEN.VENDIDO,0),0)),3)  ELSE 
			SUM(ISNULL(ST.STOCK,0))-ROUND(SUM(ISNULL(CL.UNIDADES,0)),3)-ROUND(SUM(ISNULL(ISNULL(CL.VENDIDAS,0),0)),3) END  DIFERENCIA
		, ROUND(ISNULL(MAX(CPA.COSTESTOCK),0),2) COSTO
		, ISNULL(PV.PNETO,0) COSTOVEN
		, ISNULL(PV.PNETO,0)*ROUND(SUM(ISNULL(ST.STOCK,0))-ROUND(SUM(ISNULL(CL.UNIDADES,0)),3)-ROUND(SUM(ISNULL(ISNULL(CL.VENDIDAS,0),0)),3),3) COSTOVEN_DIFERENCIA
		/*, '1|'+CAST(ART.CODARTICULO AS NVARCHAR(10))+'|'+CASE WHEN AL.TALLA IN ('@') THEN '.' ELSE  AL.TALLA END+'|'+CASE WHEN AL.TALLA IN ('@') THEN '.' ELSE  AL.COLOR END+'|'
					+CAST(ROUND(SUM(ISNULL(CL.UNIDADES,0))+SUM(ISNULL(ISNULL(CL.VENDIDAS,0),0)),3) AS nvarchar(10))+'|'+'0'+'|'+'0'+'|'+'0'+'|'+'|' TELESTOCK_INVENTARIO
		, '1|'+CAST(ART.CODARTICULO AS NVARCHAR(10))+'|'+CASE WHEN AL.TALLA IN ('@') THEN '.' ELSE  AL.TALLA END+'|'+CASE WHEN AL.TALLA IN ('@') THEN '.' ELSE  AL.COLOR END+'|'
					+CAST(ROUND(SUM(ISNULL(CL.UNIDADES,0)),3) AS nvarchar(10))+'|'+'0'+'|'+'0'+'|'+'0'+'|'+'|' TELESTOCK_CONTADO
		, '1|'+CAST(ART.CODARTICULO AS NVARCHAR(10))+'|'+CASE WHEN AL.TALLA IN ('@') THEN '.' ELSE  AL.TALLA END+'|'+CASE WHEN AL.TALLA IN ('@') THEN '.' ELSE  AL.COLOR END+'|'
					+CAST(ROUND(SUM(ISNULL(ST.STOCK,0))-ROUND(SUM(ISNULL(CL.UNIDADES,0)),3)-ROUND(SUM(ISNULL(ISNULL(CL.VENDIDAS,0),0)),3),3) AS nvarchar(10))+'|'+'0'+'|'+'0'+'|'+'0'+'|'+'|' TELESTOCK_AJUSTE*/
		
		
FROM 
		ARTICULOS ART  WITH(NOLOCK) --ON CL.CODARTICULO=ART.CODARTICULO
		INNER JOIN rip.CONTEO_TARGET CT WITH(NOLOCK) ON ART.CODARTICULO=CT.CODARTICULO AND CT.IDCONTEO=@IDCONTEO
		INNER JOIN ARTICULOSLIN AL  WITH(NOLOCK) ON ART.CODARTICULO=AL.CODARTICULO
		LEFT JOIN DEPARTAMENTO DPTO ON ART.DPTO=DPTO.NUMDPTO
		LEFT JOIN SECCIONES S WITH(NOLOCK) ON ART.DPTO=S.NUMDPTO AND ART.SECCION=S.NUMSECCION
		LEFT JOIN FAMILIAS F WITH(NOLOCK) ON ART.DPTO=F.NUMDPTO AND ART.SECCION=F.NUMSECCION AND ART.FAMILIA=F.NUMFAMILIA
		LEFT JOIN SUBFAMILIAS SF WITH(NOLOCK) ON ART.DPTO=SF.NUMDPTO AND ART.SECCION=SF.NUMSECCION AND ART.FAMILIA=SF.NUMFAMILIA AND ART.SUBFAMILIA=SF.NUMSUBFAMILIA
		LEFT JOIN CTE_CONTEO_REAL CL ON CL.CODARTICULO=AL.CODARTICULO AND CL.COLOR=AL.COLOR COLLATE Latin1_General_CS_AI AND CL.TALLA=AL.TALLA COLLATE Latin1_General_CS_AI
		LEFT JOIN CTE_CONTEO_ZONA CZ ON CZ.CODARTICULO=AL.CODARTICULO AND CZ.COLOR=AL.COLOR COLLATE Latin1_General_CS_AI AND CZ.TALLA=AL.TALLA COLLATE Latin1_General_CS_AI
		LEFT JOIN rip.CONTEOSTOCK ST WITH(NOLOCK) ON ST.CODALMACEN=@ALMACEN AND ST.FECHA=@FECHA AND AL.CODARTICULO=ST.CODARTICULO AND AL.COLOR=ST.COLOR AND AL.TALLA=ST.TALLA
		LEFT JOIN COSTESPORALMACEN CPA WITH(NOLOCK) ON AL.CODARTICULO=CPA.CODARTICULO AND AL.COLOR=CPA.COLOR AND AL.TALLA=CPA.TALLA AND CPA.CODALMACEN=@ALMACEN
		LEFT JOIN CTE_VENDIDO VEN ON  AL.CODARTICULO=VEN.CODARTICULO AND AL.COLOR=VEN.COLOR AND AL.TALLA=VEN.TALLA
		LEFT JOIN PRECIOSVENTA PV WITH(NOLOCK) ON AL.CODARTICULO=PV.CODARTICULO AND AL.COLOR=PV.COLOR AND AL.TALLA=PV.TALLA AND PV.IDTARIFAV=@TARIFA
		LEFT JOIN MARCA M WITH(NOLOCK) ON ART.MARCA=M.CODMARCA
WHERE
		(@ESTOTAL=1 AND (ST.STOCK<>0 OR CL.UNIDADES<>0 OR CL.VENDIDAS<>0))
		OR ( @ESTOTAL=0 AND (CL.UNIDADES<>0 OR CL.VENDIDAS<>0 OR VEN.VENDIDO<>0) )
	
GROUP BY
		ART.CODARTICULO
		, CASE WHEN AL.TALLA IN ('@') THEN '.' ELSE  AL.COLOR END 
		, CASE WHEN AL.TALLA IN ('@') THEN '.' ELSE  AL.TALLA END 
		, ART.REFPROVEEDOR
		, ART.DESCRIPCION
		, ART.DESCRIPADIC
		, CASE WHEN AL.TALLA IN ('@') THEN '' ELSE  AL.CODBARRAS END 
		, ISNULL(S.DESCRIPCION,'SIN SECCION')
		, ISNULL(DPTO.DESCRIPCION,'SIN DPTO')
		, ISNULL(F.DESCRIPCION,'')
		, ISNULL(SF.DESCRIPCION,'')
		, ISNULL(M.DESCRIPCION,'')
		, PV.PNETO
		, CZ.ZONA_1
		, CZ.ZONA_2
		, CZ.ZONA_3
		, CZ.ZONA_4
		, CZ.ZONA_5
		, CZ.ZONA_6
		, CZ.ZONA_7
		--, CPA.COSTESTOCK
HAVING (@SOLODIFERENCIAS=0 OR SUM(ISNULL(ST.STOCK,0))-SUM(ISNULL(CL.UNIDADES,0))-SUM(ISNULL(CL.VENDIDAS,0))<>0)
GO


