CREATE FUNCTION [rip].[RIP_FSTOCK_ARTICULO_FECHA] (@FECHA AS DATETIME, @ALMACEN AS NVARCHAR(4) )
RETURNS TABLE
WITH ENCRYPTION AS RETURN (
SELECT  A.CODARTICULO, A.COLOR, A.TALLA,
 ROUND(SUM(ISNULL(REG,0))+SUM(ISNULL(REC,0))+SUM(ISNULL(COMP,0))-SUM(ISNULL(ENV,0))-SUM(ISNULL(VEN,0))-SUM(ISNULL(CON,0))-SUM(ISNULL(FAB_SAL,0))+SUM(ISNULL(FAB_ENT,0)),3) AS STOCK,
 SUM(ISNULL(REG,0)) REG,SUM(ISNULL(REC,0)) REC,SUM(ISNULL(COMP,0)) COMP,SUM(ISNULL(ENV,0)) ENV, SUM(ISNULL(VEN,0)) VEN,SUM(ISNULL(CON,0)) CON, SUM(ISNULL(FAB_ENT,0)) FAB_ENT
 , SUM(ISNULL(FAB_SAL,0)) FAB_SAL
 FROM (
SELECT CODALMACENORIGEN AS CODALMACEN, A.CODARTICULO, A.COLOR, A.TALLA,0 AS FECHA, 0.0 AS REG,
0.0 AS REC, 0.0 AS COMP, 0.0 AS ENV, 0.0 AS VEN, SUM(UNIDADES) AS CON, 0.0 FAB_SAL, 0.0 FAB_ENT
FROM (SELECT A.* FROM MOVIMENTS AS A INNER JOIN CONSUMOSCAB AS B
ON A.SERIEDOC=B.SERIE AND A.NUMDOC=B.NUMERO AND A.TIPO='CON'  WHERE B.FECHA <= @FECHA ) AS A
LEFT JOIN ( SELECT CODALMACENORIGEN AS CODALMACEN, CODARTICULO, COLOR, TALLA, MAX(FECHA) AS FECHA
FROM MOVIMENTS  WHERE  TIPO='REG' AND FECHA <= @FECHA
GROUP BY CODALMACENORIGEN, CODARTICULO, COLOR, TALLA
)AS B ON A.CODALMACENORIGEN=B.CODALMACEN AND A.CODARTICULO=B.CODARTICULO AND A.TALLA=B.TALLA AND A.COLOR=B.COLOR
WHERE A.FECHA>=B.FECHA OR B.FECHA IS NULL 
GROUP BY CODALMACENORIGEN, A.CODARTICULO, A.COLOR, A.TALLA
UNION ALL
SELECT CODALMACENORIGEN AS CODALMACEN, A.CODARTICULO, A.COLOR, A.TALLA,0 AS FECHA, 0 AS REG,
0 AS REC, 0 AS COMP, SUM(UNIDADES) AS ENV, 0 AS VEN, 0 AS CON, 0 FAB_SAL, 0 FAB_ENT
FROM (SELECT A.* FROM MOVIMENTS AS A INNER JOIN TRASPASOSCAB AS B
ON A.SERIEDOC=B.SERIE AND A.NUMDOC=B.NUMERO AND A.CAJA=B.CAJA WHERE A.TIPO='ENV' AND B.FECHA <= @FECHA ) AS A
LEFT JOIN ( SELECT CODALMACENORIGEN AS CODALMACEN, CODARTICULO, COLOR, TALLA, MAX(FECHA) AS FECHA
FROM MOVIMENTS  WHERE  TIPO='REG' AND FECHA <= @FECHA
GROUP BY CODALMACENORIGEN, CODARTICULO, COLOR, TALLA
)AS B ON A.CODALMACENORIGEN=B.CODALMACEN AND A.CODARTICULO=B.CODARTICULO AND A.TALLA=B.TALLA AND A.COLOR=B.COLOR
WHERE A.FECHA>=B.FECHA OR B.FECHA IS NULL
GROUP BY CODALMACENORIGEN, A.CODARTICULO, A.COLOR, A.TALLA

UNION ALL
SELECT CODALMACENDESTINO AS CODALMACEN, A.CODARTICULO, A.COLOR, A.TALLA, MAX(A.FECHA) AS FECHA,  0 AS REG,
SUM(UNIDADES) AS REC, 0 AS COMP, 0  AS ENV, 0 AS VEN, 0 AS CON, 0 FAB_SAL, 0 FAB_ENT
FROM (SELECT A.*, B.FECHARECIBIDO FROM MOVIMENTS AS A INNER JOIN TRASPASOSCAB AS B
ON A.SERIEDOC=B.SERIE AND A.NUMDOC=B.NUMERO AND A.CAJA=B.CAJA WHERE A.TIPO='ENV' AND B.FECHARECIBIDO <= @FECHA AND B.RECIBIDO='T' ) AS A
LEFT JOIN ( SELECT CODALMACENORIGEN AS CODALMACEN, CODARTICULO, COLOR, TALLA, MAX(FECHA) AS FECHA
FROM MOVIMENTS  WHERE  TIPO='REG' AND FECHA <= @FECHA
GROUP BY CODALMACENORIGEN, CODARTICULO, COLOR, TALLA
)AS C ON A.CODALMACENDESTINO=C.CODALMACEN AND A.CODARTICULO=C.CODARTICULO AND A.TALLA=C.TALLA AND A.COLOR=C.COLOR
WHERE A.FECHARECIBIDO>=C.FECHA OR C.FECHA IS NULL AND UNIDADES<>0
GROUP BY CODALMACENDESTINO, A.CODARTICULO,A.COLOR, A.TALLA
UNION ALL
SELECT A.* FROM (
SELECT CODALMACENORIGEN AS CODALMACEN, CODARTICULO, COLOR, TALLA, FECHA, SUM(UNIDADES) AS REG,
0 AS REC, 0 AS COMP, 0  AS ENV, 0 AS VEN, 0 AS CON, 0 FAB_SAL, 0 FAB_ENT
FROM MOVIMENTS  WHERE   TIPO='REG'  AND  FECHA <= @FECHA
GROUP BY CODALMACENORIGEN, CODARTICULO, COLOR, TALLA, FECHA) AS A INNER JOIN
(SELECT CODALMACENORIGEN AS CODALMACEN, CODARTICULO, COLOR, TALLA, MAX(FECHA) AS FECHA
FROM MOVIMENTS  WHERE   TIPO='REG' AND  FECHA <= @FECHA
GROUP BY CODALMACENORIGEN, CODARTICULO, COLOR, TALLA) AS B
ON A.CODALMACEN=B.CODALMACEN AND A.CODARTICULO=B.CODARTICULO AND A.COLOR=B.COLOR AND A.TALLA=B.TALLA
AND A.FECHA=B.FECHA
UNION ALL
SELECT B.CODALMACEN, B.CODARTICULO,B.COLOR, B.TALLA, 0 AS FECHA, 0 AS REG,0 AS REC,
SUM(COMP) AS COMP, 0 AS ENV, 0 AS VEN, 0 AS CON, 0 FAB_SAL, 0 FAB_ENT FROM
(SELECT B.CODALMACEN, B.CODARTICULO, B.COLOR, B.TALLA,  FECHAALBARAN AS FECHA,  SUM(UNIDADESTOTAL) AS COMP
FROM ALBCOMPRACAB AS A INNER JOIN ALBCOMPRALIN AS B ON A.NUMSERIE=B.NUMSERIE AND A.NUMALBARAN=B.NUMALBARAN
WHERE  FECHAALBARAN <= @FECHA
GROUP BY B.CODALMACEN, B.CODARTICULO, B.COLOR, B.TALLA, UNIDADESTOTAL, FECHAALBARAN ) AS B
LEFT JOIN ( SELECT CODALMACENORIGEN AS CODALMACEN, CODARTICULO, COLOR, TALLA, MAX(FECHA) AS FECHA
FROM MOVIMENTS  WHERE  TIPO='REG' AND  FECHA <= @FECHA
